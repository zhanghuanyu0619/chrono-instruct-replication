#!/usr/bin/env python3
"""Collect every per-vintage results/chrono-instruct-*/eval.json into one place for
the replication report. Writes:
  - results/replication-report/eval_results.json  (machine-readable, all vintages)
  - results/replication-report/eval_summary.md    (the report-facing tables)

Run after `run_eval.py` for each vintage (eval_all_vintages.sh does both). Pure
stdlib; safe to run with only a subset of vintages present.
"""
import glob
import json
import os

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
RESULTS = os.path.join(REPO_ROOT, "results")
REPORT = os.path.join(RESULTS, "replication-report")


def main():
    paths = sorted(glob.glob(os.path.join(RESULTS, "chrono-instruct-*", "eval.json")))
    runs = []
    for p in paths:
        with open(p) as f:
            runs.append(json.load(f))
    runs.sort(key=lambda r: r["vintage"])
    if not runs:
        print("[aggregate] no eval.json files found — run scripts/run_eval.py first")
        return

    os.makedirs(REPORT, exist_ok=True)
    with open(os.path.join(REPORT, "eval_results.json"), "w") as f:
        json.dump(runs, f, indent=2)

    L = ["# Evaluation results — ChronoGPT-Instruct sweep",
         "",
         "Auto-generated by `scripts/aggregate_eval.py` from each",
         "`results/chrono-instruct-*/eval.json`. Do not edit by hand — re-run the",
         "eval sweep to refresh.",
         "",
         "**Reading the consistency tests (Tables 2-3).** A chronologically consistent",
         "model should be **correct before its cutoff** and **wrong after it**. So the",
         "target pattern is high pre-cutoff accuracy and **0/N post-cutoff** — being",
         "*wrong* on post-cutoff items is the no-look-ahead guarantee working, not a",
         "failure.",
         "",
         "## Table 2 — U.S. president prediction",
         "",
         "| Vintage | Pre-cutoff correct | Post-cutoff correct (want 0/N) | Consistent rows |",
         "|--------:|:------------------:|:------------------------------:|:---------------:|"]
    for r in runs:
        s = r["table2_president"]["summary"]
        L.append(f"| {r['vintage']} | {s['pre_cutoff_correct']} | {s['post_cutoff_correct']} | {s['consistent_rows']} |")

    L += ["",
          "## Table 3 — Major world events",
          "",
          "| Vintage | Pre-cutoff correct | Post-cutoff correct (want 0/N) | Consistent rows |",
          "|--------:|:------------------:|:------------------------------:|:---------------:|"]
    for r in runs:
        s = r["table3_major_events"]["summary"]
        L.append(f"| {r['vintage']} | {s['pre_cutoff_correct']} | {s['post_cutoff_correct']} | {s['consistent_rows']} |")

    if any("figure3_alpacaeval" in r for r in runs):
        L += ["",
              "## Figure 3 — AlpacaEval length-controlled win-rate vs Qwen-1.5-1.8B-Chat",
              "",
              "| Vintage | LC win-rate (%) | #instructions |",
              "|--------:|:---------------:|:-------------:|"]
        for r in runs:
            f3 = r.get("figure3_alpacaeval", {})
            wr = f3.get("lc_winrate", "—")
            L.append(f"| {r['vintage']} | {wr} | {f3.get('n', '—')} |")

    L += ["", f"_Collected from {len(runs)} vintage(s): "
          f"{', '.join(str(r['vintage']) for r in runs)}._", ""]
    with open(os.path.join(REPORT, "eval_summary.md"), "w") as f:
        f.write("\n".join(L))
    print(f"[aggregate] wrote {os.path.join(REPORT, 'eval_results.json')} and eval_summary.md ({len(runs)} vintages)")


if __name__ == "__main__":
    main()
